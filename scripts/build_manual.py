def _set_rtl_run(run):
    rPr = run._r.get_or_add_rPr()
    rtl = OxmlElement('w:rtl'); rtl.set(qn('w:val'), '1'); rPr.append(rtl)

def _set_rtl_par(p):
    pPr = p._p.get_or_add_pPr()
    bidi = OxmlElement('w:bidi'); bidi.set(qn('w:val'), '1'); pPr.append(bidi)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT

def _shade(p, color_hex):
    pPr = p._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto'); shd.set(qn('w:fill'), color_hex)
    pPr.append(shd)

def _border(p):
    pPr = p._p.get_or_add_pPr()
    pbdr = OxmlElement('w:pBdr')
    for edge in ('top', 'left', 'bottom', 'right'):
        e = OxmlElement('w:' + edge)
        e.set(qn('w:val'), 'single'); e.set(qn('w:sz'), '6'); e.set(qn('w:space'), '6'); e.set(qn('w:color'), 'CBD5E1')
        pbdr.append(e)
    pPr.append(pbdr)

def para(text='', size=11, bold=False, color=None, italic=False, space_after=6, space_before=0):
    p = doc.add_paragraph()
    _set_rtl_par(p)
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.space_before = Pt(space_before)
    if text:
        r = p.add_run(text)
        r.bold = bold; r.italic = italic; r.font.size = Pt(size)
        if color: r.font.color.rgb = color
        _set_rtl_run(r)
    return p

def rich(parts, space_after=6):
    """parts = list of (text, {bold,color,italic,size})"""
    p = doc.add_paragraph(); _set_rtl_par(p); p.paragraph_format.space_after = Pt(space_after)
    for text, opt in parts:
        r = p.add_run(text)
        r.bold = opt.get('bold', False); r.italic = opt.get('italic', False)
        r.font.size = Pt(opt.get('size', 11))
        if opt.get('color'): r.font.color.rgb = opt['color']
        _set_rtl_run(r)
    return p

def bullet(text, level=0, bold_lead=None):
    p = doc.add_paragraph(style='List Bullet' if level == 0 else 'List Bullet 2')
    _set_rtl_par(p); p.paragraph_format.space_after = Pt(3)
    if bold_lead:
        r = p.add_run(bold_lead); r.bold = True; _set_rtl_run(r)
        r2 = p.add_run(text); _set_rtl_run(r2)
    else:
        r = p.add_run(text); _set_rtl_run(r)
    return p

def h1(text):
    doc.add_page_break()
    p = doc.add_paragraph(); _set_rtl_par(p)
    p.paragraph_format.space_before = Pt(4); p.paragraph_format.space_after = Pt(10)
    r = p.add_run(text); r.bold = True; r.font.size = Pt(20); r.font.color.rgb = BRAND; _set_rtl_run(r)
    # underline rule
    pPr = p._p.get_or_add_pPr(); pbdr = OxmlElement('w:pBdr')
    b = OxmlElement('w:bottom'); b.set(qn('w:val'), 'single'); b.set(qn('w:sz'), '12'); b.set(qn('w:space'), '4'); b.set(qn('w:color'), '1E40AF')
    pbdr.append(b); pPr.append(pbdr)
    return p

